package Collection_Concepts;

import java.util.HashSet;

public class Hash_Set {

	public static void main(String[] args) {
		HashSet<String> set = new HashSet<String>();
		set.add("car");
		set.add("bike");
		set.add("cycle");
		set.add("auto");
		set.add("plane");
		set.add("lorry");
		set.add("lorry");
		System.out.println(set);
		
	}

}

